#ejemplos de distintos metodos

#solo tiene un parametro: el final al que se le resta 1
for x in range(6):
    print(x)

#tiene 2 paramentros: el inicio y el fin
for x in range(0, 6):
    print(x)

#tiene 3 parametros: inicio, fin y de cuanto en cuanto
#ejemplo (de dos en dos): 2, 4, 6, 8,...
for x in range(0, 6, 1):
    print(x)

#ciclo FOR
for x in range(0, 21, 2):
    print(x)
